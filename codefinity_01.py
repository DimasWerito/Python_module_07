"""
Suppose you run a small grocery store with daily expenses of $2000. 
If your daily revenue is less than $2000, you're operating at a loss; 
otherwise, you're turning a profit. Let's craft an if/else statement to reflect this situation.

Task
Set up a conditional statement to determine if the revenue variable is below 2000.

If it is, display the message "We're operating at a loss!"
Otherwise, display the message "Everything is good!"
Please refrain from altering the revenue value.
"""


revenue = 2000

# Check if revenue is less than 2000
if revenue < 2000:
  # Output We're operating at a loss!
    print("We're operating at a loss!")
else:
  # Output Everything is good!
    print("Everything is good!")